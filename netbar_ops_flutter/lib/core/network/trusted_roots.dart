/// 只构建一次：SecurityContext 创建有开销，且内容不变。
SecurityContext? _cachedContext;

/// 创建带内置根证书的 HttpClient（供 dio 的 IOHttpClientAdapter 使用）。
/// SecurityContext 构建失败时回退系统默认信任库（不阻断业务）并记 ERROR。
HttpClient createHttpClientWithBundledRoots() {
  try {
    final ctx = _cachedContext ??= (SecurityContext(withTrustedRoots: true)
      ..setTrustedCertificatesBytes(utf8.encode(_isrgRootX1Pem)));
    return HttpClient(context: ctx);
  } catch (e) {
    WebRtcCrashLogger.I.log('ERROR', 'http', 'trustedRoots', '-',
        'bundled root init failed, fallback to system store: $e');
    return HttpClient();
  }
}
